#!/usr/local/bin/env python

#=============================================================================================
# MODULE DOCSTRING
#=============================================================================================

"""
Analyze YANK output file.

"""

#=============================================================================================
# MODULE IMPORTS
#=============================================================================================

from yank import utils

#=============================================================================================
# COMMAND DISPATCH
#=============================================================================================

def dispatch(args):
    from yank import analyze
    verbose = args['--verbose']
    utils.config_root_logger(verbose)
    analyze.analyze(args['--store'], verbose=verbose)
    return True
